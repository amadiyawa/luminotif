package com.amadiyawa.feature_base.presentation.navigation

/**
 * Enum class representing the possible placements for navigation destinations
 * within the application's UI.
 */
enum class DestinationPlacement {
    /**
     * Represents a destination that should be placed in the BottomBar navigation.
     */
    BottomBar,

    /**
     * Represents a destination that should be placed in the NavRail navigation.
     */
    NavRail
}